#include "ft_printf.h"

void	ft_putnbr(int n, int *len)
{
	if (n < 0)
	{
		ft_putchar('-', len);
		if (n == -2147483648)
		{
			ft_putchar('2', len);
			ft_convert(147483648, "0123456789", len);
		}
		else
		{
			n = n * -1;
			ft_convert(n, "0123456789", len);
		}
	}
	else
		ft_convert(n, "0123456789", len);
}
